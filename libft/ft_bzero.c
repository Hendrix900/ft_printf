#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	int				l;
	unsigned char	*q;

	q = (unsigned char*)s;
	l = 0;
	while (n > 0)
	{
		q[l] = 0;
		l++;
		n--;
	}
}
